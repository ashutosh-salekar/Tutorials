# We can print data to console using "print" keyword
print("Hello World !")

# Variables are use to store the data and perform various operations while executing the code.

a = 5  # we have assign the value 5 to the variable "a".
print("a = ",a)

b = a + 5
print("b = ",b) 

# Multiple variable assignment
p,q,r = 10,20,30
print("p:",p," q:",q," r:",r)

# Memory address of variable. We use "id" keyword
data = 15
print(data,id(data))
